pub enum Winner {
    X,
    O,
    Draw,
}

pub struct TicTacToeAnimation {
    winner: Winner,

    last_frame: Instant,
    frame: u32,
}

impl TicTacToeAnimation {
    pub fn new(winner: Winner) -> Self {
        Self {
            winner,

            last_frame: Instant::now(),
            frame: 0,
        }
    }
}

impl Animation for TicTacToeAnimation {
    fn should_execute(&self) -> bool {
        // Target: 40 FPS
        self.last_frame.elapsed() > Duration::from_millis(25)
    }

    fn next_frame(&mut self) -> Option<RgbImage> {
        let mut result = ImageBuffer::new();

        let pixel_fn = |(point, color)| Pixel(point, color);

        match self.winner {
            Winner::X | Winner::O => {
                let converter_builder = |invert: bool| {
                    move |b: &u8| {
                        if *b > 0 {
                            if invert {
                                Rgb888::CSS_GRAY
                            } else {
                                Rgb888::WHITE
                            }
                        } else {
                            Rgb888::BLACK
                        }
                    }
                };

                let show_upper = self.frame / 8 % 2 == 0;
                let map_upper = converter_builder(!show_upper);
                let map_lower = converter_builder(show_upper);

                let (winner_symbol, loser_symbol) = if matches!(self.winner, Winner::X) {
                    (X, O)
                } else {
                    (O, X)
                };

                let winner_symbol = winner_symbol.iter().map(map_upper);
                let loser_symbol = loser_symbol.iter().map(map_lower);
                let winner = WINNER_MASK.iter().map(map_upper);
                let loser = LOSER_MASK.iter().map(map_lower);

                result
                    .draw_iter(
                        Rectangle::new(Point::new(6, 7), Size::new(7, 7))
                            .points()
                            .zip(winner_symbol)
                            .map(pixel_fn),
                    )
                    .unwrap();
                result
                    .draw_iter(
                        Rectangle::new(Point::new(6, 18), Size::new(7, 7))
                            .points()
                            .zip(loser_symbol)
                            .map(pixel_fn),
                    )
                    .unwrap();

                result
                    .draw_iter(
                        Rectangle::new(Point::new(26, 7), Size::new(34, 7))
                            .points()
                            .zip(winner)
                            .map(pixel_fn),
                    )
                    .unwrap();
                result
                    .draw_iter(
                        Rectangle::new(Point::new(26, 18), Size::new(27, 7))
                            .points()
                            .zip(loser)
                            .map(pixel_fn),
                    )
                    .unwrap();
            }

            Winner::Draw => {
                if (self.frame / 8) % 2 == 0 {
                    result
                        .draw_iter(
                            Rectangle::new(Point::new(0, 0), Size::new(64, 32))
                                .points()
                                .zip(DRAW_MASK.iter().map(|b| {
                                    if *b > 0 {
                                        Rgb888::WHITE
                                    } else {
                                        Rgb888::BLACK
                                    }
                                }))
                                .map(pixel_fn),
                        )
                        .unwrap()
                } else {
                    result
                        .draw_iter(
                            Rectangle::new(Point::new(0, 0), Size::new(64, 32))
                                .points()
                                .zip(DRAW_MASK.iter().map(|b| {
                                    if *b > 0 {
                                        Rgb888::BLACK
                                    } else {
                                        Rgb888::WHITE
                                    }
                                }))
                                .map(pixel_fn),
                        )
                        .unwrap()
                }
            }
        }

        self.frame += 1;
        self.last_frame = Instant::now();

        Some(result.into_image())
    }
}
